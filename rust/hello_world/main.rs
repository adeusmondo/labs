// fn - Define a function in Rust
// main - It's the name of the function, in this case 'main' is the entry point of the program
// () - It's the parameter list, in this case it's empty
// {} - It's the body of the function
// println! - It's a macro (macro is defined by the ! in the end of function name) that prints a string to the console
// "Hello, world!" - It's the string that will be printed to the console
fn main() {
  println!("Hello, world!");
}
